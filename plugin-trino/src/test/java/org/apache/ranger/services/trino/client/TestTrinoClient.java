    @Test
    void test64_initConnection_securityManagerBlocksDriver_throwsHadoopException() {
        final SecurityManager previous = System.getSecurityManager();
        try {
            System.setSecurityManager(new SecurityManager() {
                @Override
                public void checkPermission(java.security.Permission perm) {
                    if (previous != null) {
                        previous.checkPermission(perm);
                    }
                }

                @Override
                public void checkPackageAccess(String pkg) {
                    if (pkg != null && pkg.startsWith("io.trino")) {
                        throw new SecurityException("blocked trino driver package");
                    }
                    if (previous != null) {
                        previous.checkPackageAccess(pkg);
                    }
                }
            });
            try (MockedStatic<Subject> subjectStatic = Mockito.mockStatic(Subject.class)) {
                subjectStatic.when(() -> Subject.doAs(any(), any(PrivilegedAction.class)))
                        .thenAnswer(inv -> {
                            PrivilegedAction<?> action = inv.getArgument(1);
                            return action.run();
                        });
                assertThrows(HadoopException.class,
                        () -> new NoConnectionTrinoClient("svc", baseConnProps()));
            }
        } finally {
            System.setSecurityManager(previous);
        }
    }

