@ExtendWith(MockitoExtension.class)
@TestMethodOrder(MethodOrderer.MethodName.class)
class TestNiFiConnectionMgrSslKeystoreLoad {
    @Test
    void getNiFiClient_ssl_loadsKeystoresCreatedWithKeytool(@TempDir Path tempDir) throws Exception {
        Path keytool = Path.of(System.getProperty("java.home"), "bin", "keytool");
        Assumptions.assumeTrue(Files.exists(keytool), "JDK keytool not found at " + keytool);

        Path keystore = tempDir.resolve("keystore.jks");
        Path truststore = tempDir.resolve("truststore.jks");
        Path exportedCert = tempDir.resolve("nifi.cer");
        String password = "changeit";

        Process gen = new ProcessBuilder(
                keytool.toString(),
                "-genkeypair",
                "-alias",
                "nifi",
                "-keyalg",
                "RSA",
                "-keysize",
                "2048",
                "-storetype",
                "JKS",
                "-keystore",
                keystore.toString(),
                "-storepass",
                password,
                "-keypass",
                password,
                "-dname",
                "CN=localhost",
                "-validity",
                "365")
                .redirectError(ProcessBuilder.Redirect.PIPE)
                .start();
        Assumptions.assumeTrue(gen.waitFor(120, TimeUnit.SECONDS), "keytool genkeypair timed out");
        Assumptions.assumeTrue(gen.exitValue() == 0, "keytool genkeypair failed");

        Process exp = new ProcessBuilder(
                keytool.toString(),
                "-exportcert",
                "-alias",
                "nifi",
                "-keystore",
                keystore.toString(),
                "-storepass",
                password,
                "-file",
                exportedCert.toString())
                .redirectError(ProcessBuilder.Redirect.PIPE)
                .start();
        Assumptions.assumeTrue(exp.waitFor(60, TimeUnit.SECONDS), "keytool exportcert timed out");
        Assumptions.assumeTrue(exp.exitValue() == 0, "keytool exportcert failed");

        Process imp = new ProcessBuilder(
                keytool.toString(),
                "-importcert",
                "-noprompt",
                "-alias",
                "nifi",
                "-file",
                exportedCert.toString(),
                "-keystore",
                truststore.toString(),
                "-storepass",
                password,
                "-storetype",
                "JKS")
                .redirectError(ProcessBuilder.Redirect.PIPE)
                .start();
        Assumptions.assumeTrue(imp.waitFor(60, TimeUnit.SECONDS), "keytool importcert timed out");
        Assumptions.assumeTrue(imp.exitValue() == 0, "keytool importcert failed");

        Map<String, String> configs = new HashMap<>();
        configs.put(NiFiConfigs.NIFI_URL, "https://localhost:8443/nifi-api/resources");
        configs.put(NiFiConfigs.NIFI_AUTHENTICATION_TYPE, NiFiAuthType.SSL.name());
        configs.put(NiFiConfigs.NIFI_SSL_KEYSTORE, keystore.toString());
        configs.put(NiFiConfigs.NIFI_SSL_KEYSTORE_TYPE, "JKS");
        configs.put(NiFiConfigs.NIFI_SSL_KEYSTORE_PASSWORD, password);
        configs.put(NiFiConfigs.NIFI_SSL_TRUSTSTORE, truststore.toString());
        configs.put(NiFiConfigs.NIFI_SSL_TRUSTSTORE_TYPE, "JKS");
        configs.put(NiFiConfigs.NIFI_SSL_TRUSTSTORE_PASSWORD, password);

        NiFiClient client = NiFiConnectionMgr.getNiFiClient("svc", configs);

        assertNotNull(client.getSslContext());
        assertEquals("https://localhost:8443/nifi-api/resources", client.getUrl());
    }
}
