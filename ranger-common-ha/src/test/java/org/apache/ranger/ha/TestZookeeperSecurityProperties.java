package org.apache.ranger.ha;

import org.apache.zookeeper.ZooDefs;
import org.apache.zookeeper.data.ACL;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.MethodOrderer;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.TestMethodOrder;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;

/**
 * @generated by Cursor
 * @description : Unit Test cases for ZookeeperSecurityProperties
 */
@ExtendWith(MockitoExtension.class)
@TestMethodOrder(MethodOrderer.MethodName.class)
class TestZookeeperSecurityProperties {
    @Test
    void testParseAcl_oneArg_negative_invalidFormatThrows() {
        Assertions.assertThrows(IllegalArgumentException.class, () -> ZookeeperSecurityProperties.parseAcl("schemeOnly"));
    }

    @Test
    void testParseAcl_oneArg_positive_parsesSchemeAndId() {
        ACL acl = ZookeeperSecurityProperties.parseAcl("world:anyone");

        Assertions.assertEquals(ZooDefs.Perms.ALL, acl.getPerms());
        Assertions.assertEquals("world", acl.getId().getScheme());
        Assertions.assertEquals("anyone", acl.getId().getId());
    }

    @Test
    void testParseAcl_twoArg_negative_nonEmptyInvalidThrows() {
        ACL fallback = ZooDefs.Ids.READ_ACL_UNSAFE.get(0);

        Assertions.assertThrows(IllegalArgumentException.class, () -> ZookeeperSecurityProperties.parseAcl("bad", fallback));
    }

    @Test
    void testParseAcl_twoArg_positive_emptyUsesDefaultAcl() {
        ACL fallback = ZooDefs.Ids.READ_ACL_UNSAFE.get(0);

        ACL acl = ZookeeperSecurityProperties.parseAcl("", fallback);

        Assertions.assertSame(fallback, acl);
    }

    @Test
    void testParseAuth_negative_invalidFormatThrows() {
        Assertions.assertThrows(IllegalArgumentException.class, () -> ZookeeperSecurityProperties.parseAuth("schemeOnly"));
    }

    @Test
    void testParseAuth_positive_parsesSchemeAndSecret() {
        Assertions.assertNotNull(ZookeeperSecurityProperties.parseAuth("digest:user:hash"));
    }
}
