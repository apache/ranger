    @Test
    public void test01_GettersSettersAndToString() {
        UnixSyncSourceInfo info = new UnixSyncSourceInfo();
        info.setUnixBackend("nss");
        info.setFileName("/etc/passwd");
        info.setSyncTime("2025-09-17T10:00:00Z");
        info.setLastModified("2025-09-17T09:00:00Z");
        info.setMinUserId("1000");
        info.setMinGroupId("1000");
        info.setTotalUsersSynced(50);
        info.setTotalGroupsSynced(10);
        info.setTotalUsersDeleted(4);
        info.setTotalGroupsDeleted(2);

        Assertions.assertEquals("nss", info.getUnixBackend());
        Assertions.assertEquals("/etc/passwd", info.getFileName());
        Assertions.assertEquals("2025-09-17T10:00:00Z", info.getSyncTime());
        Assertions.assertEquals("2025-09-17T09:00:00Z", info.getLastModified());
        Assertions.assertEquals("1000", info.getMinUserId());
        Assertions.assertEquals("1000", info.getMinGroupId());
        Assertions.assertEquals(50, info.getTotalUsersSynced());
        Assertions.assertEquals(10, info.getTotalGroupsSynced());
        Assertions.assertEquals(4, info.getTotalUsersDeleted());
        Assertions.assertEquals(2, info.getTotalGroupsDeleted());

        String str = info.toString();
        Assertions.assertTrue(str.contains("UnixSycnSourceInfo"));
        Assertions.assertTrue(str.contains("nss"));
        Assertions.assertTrue(str.contains("/etc/passwd"));
        Assertions.assertTrue(str.contains("1000"));

        StringBuilder sb = new StringBuilder();
        info.toString(sb);
        Assertions.assertEquals(str, sb.toString());
    }
