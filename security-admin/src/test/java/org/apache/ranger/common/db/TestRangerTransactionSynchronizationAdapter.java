package org.apache.ranger.common.db;

import org.junit.jupiter.api.MethodOrderer;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.TestMethodOrder;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;
import org.springframework.transaction.PlatformTransactionManager;
import org.springframework.transaction.support.TransactionSynchronization;
import org.springframework.transaction.support.TransactionSynchronizationManager;

import java.util.concurrent.atomic.AtomicInteger;

import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.mockito.Mockito.mock;

/**
 * @generated by Cursor
 * @description : Unit Test cases for RangerTransactionSynchronizationAdapter
 */

@ExtendWith(MockitoExtension.class)
@TestMethodOrder(MethodOrderer.MethodName.class)
public class TestRangerTransactionSynchronizationAdapter {
    @Test
    public void test1_nestedRunnableAfterCompletion() {
        TransactionSynchronizationManager.initSynchronization();
        try {
            RangerTransactionSynchronizationAdapter rtsa = new RangerTransactionSynchronizationAdapter();
            rtsa.txManager = mock(PlatformTransactionManager.class);
            AtomicInteger count = new AtomicInteger(0);
            rtsa.executeOnTransactionCompletion(() -> {
                count.incrementAndGet();
                rtsa.executeOnTransactionCompletion(count::incrementAndGet);
            });
            rtsa.afterCompletion(TransactionSynchronization.STATUS_ROLLED_BACK);
            assertEquals(1, count.get());
        } finally {
            TransactionSynchronizationManager.clear();
        }
    }

    @Test
    public void test2_executeOnTransactionCommit_onlyRunsOnCommit() {
        TransactionSynchronizationManager.initSynchronization();
        try {
            RangerTransactionSynchronizationAdapter rtsa = new RangerTransactionSynchronizationAdapter();
            rtsa.txManager = mock(PlatformTransactionManager.class);
            AtomicInteger count = new AtomicInteger(0);
            rtsa.executeOnTransactionCommit(count::incrementAndGet);
            // simulate rollback: should not run
            rtsa.afterCompletion(TransactionSynchronization.STATUS_ROLLED_BACK);
            assertEquals(0, count.get());
            // simulate commit: should run
            rtsa.executeOnTransactionCommit(count::incrementAndGet);
            rtsa.afterCompletion(TransactionSynchronization.STATUS_COMMITTED);
            assertEquals(1, count.get());
        } finally {
            TransactionSynchronizationManager.clear();
        }
    }

    @Test
    public void test3_executeOnTransactionCompletion_runsImmediatelyWhenSyncInactive() {
        RangerTransactionSynchronizationAdapter rtsa = new RangerTransactionSynchronizationAdapter();
        rtsa.txManager = mock(PlatformTransactionManager.class);
        AtomicInteger count = new AtomicInteger(0);
        rtsa.executeOnTransactionCompletion(count::incrementAndGet);
        assertEquals(1, count.get());
    }
}
